def check_preflop(player_hand):
    
    card1_rank, card1_suit = rank_map[player_hand[0][0]], suit_map[player_hand[0][1]]
    card2_rank, card2_suit = rank_map[player_hand[1][0]], suit_map[player_hand[1][1]]
    
    preflop_equity = 0 
    
    rank_order = "23456789TJQKA"

    if rank_order.index(card1_rank) < rank_order.index(card2_rank):
        card1_rank, card2_rank = card2_rank, card1_rank
        
    if card1_suit == card2_suit and card1_rank != card2_rank:
        card1_suit = "s"
        card2_suit = "s"
    else:
        card1_suit = "s"
        card2_suit = "h"
    
    preflop_equity = preflop_equity_map[((card1_rank+card1_suit, card2_rank+card2_suit))]
    return preflop_equity
        
    
    
    
def convert_card_notation(chosen_cards):
    res = []
    for card in chosen_cards:
        res.append((rank_map[card[0]] + suit_map[card[1]]))
    
    return res


